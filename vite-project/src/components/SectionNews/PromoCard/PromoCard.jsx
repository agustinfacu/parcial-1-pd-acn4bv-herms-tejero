// ============================================================================
// Componente: PromoCard
// Ubicaci√≥n : src/components/SectionNews/PromoCard/PromoCard.jsx
// Descripci√≥n:
//   - Renderiza una "tarjeta de promoci√≥n" (descuento + CTA).
//   - Se pens√≥ para convivir con NewsCard dentro de CardList.
//   - Usa atributos data-* para facilitar filtros (categor√≠as, descuento).
//   - Accesibilidad: role="article", aria-label y <time> con datetime.
//
// Props sugeridas:
//   - label         (string)  ‚Üí Texto corto superior (ej: "‚ö° Oferta Especial")
//   - headline      (string)  ‚Üí T√≠tulo fuerte (ej: "20% de descuento...")
//   - description   (string)  ‚Üí Bajada/explicaci√≥n
//   - ratingText    (string)  ‚Üí Texto de valoraci√≥n (ej: "‚òÖ‚òÖ‚òÖ‚òÖ‚òÖ  +1000 clientes")
//   - dateISO       (string)  ‚Üí Fecha ISO para <time datetime="">
//   - dateLabel     (string)  ‚Üí Fecha visible (ej: "23 de enero de 2025")
//   - discount      (number)  ‚Üí Descuento num√©rico (ej: 20, 30, 15)
//   - categories    (string[])-> Categor√≠as (ej: ["varias","copias a color"])
//   - ctaText       (string)  ‚Üí Texto del bot√≥n (ej: "Obtener Descuento")
//   - ctaHref       (string)  ‚Üí Enlace del bot√≥n (ej: "#descuento")
//   - onCtaClick    (func)    ‚Üí Callback opcional al click del CTA
//
// Nota de estilos:
//   - Este componente importa su propio CSS (PromoCard.css) con clases
//     "scoped" al componente para evitar choques con css.txt.
// ============================================================================

import React from "react";
import PropTypes from "prop-types";
import "./PromoCard.css";

const PromoCard = ({
  label = "üéâ Promoci√≥n",
  headline,
  description,
  ratingText,
  dateISO,
  dateLabel,
  discount,
  categories = [],
  ctaText = "Ver detalle",
  ctaHref = "#",
  onCtaClick,
}) => {
  // --------------------------------------------------------------------------
  // Seguridad/defensiva: si no hay headline, no tiene sentido renderizar la card
  // --------------------------------------------------------------------------
  if (!headline) return null;

  // Construimos los data-* como strings (para futuros filtros en CardList)
  const catsString = Array.isArray(categories) ? categories.join(",") : "";

  const handleClick = (e) => {
    if (typeof onCtaClick === "function") {
      onCtaClick(e);
    }
  };

  return (
    <div
      className="promo"
      role="article"
      aria-label={headline}
      data-discount={typeof discount === "number" ? discount : undefined}
      data-cats={catsString}
    >
      {/* Columna izquierda: textos de la promo */}
      <div className="left">
        {/* Etiqueta corta arriba (√≠cono + texto breve) */}
        {label && <div className="promo-label">{label}</div>}

        {/* T√≠tulo fuerte / beneficio principal */}
        <div className="promo-headline">{headline}</div>

        {/* Descripci√≥n corta de soporte */}
        {description && <div className="promo-desc">{description}</div>}

        {/* ‚ÄúValoraci√≥n‚Äù (texto libre: estrellas, claims, etc.) */}
        {ratingText && <div className="promo-rating">{ratingText}</div>}

        {/* Fecha de publicaci√≥n con √≠cono cuadrado simple (ligero) */}
        {(dateISO || dateLabel) && (
          <div className="promo-date">
            <svg
              width="14"
              height="14"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              aria-hidden="true"
            >
              <path d="M8 7h8v8H8z" strokeWidth="1.4" />
            </svg>
            <time dateTime={dateISO || ""}>{dateLabel || dateISO}</time>
          </div>
        )}
      </div>

      {/* Columna derecha: CTA + meta secundaria */}
      <div className="promo-right">
        <a className="cta-mini" href={ctaHref} onClick={handleClick}>
          {ctaText}
        </a>
        {/* Texto peque√±o en gris opcional */}
        <div className="promo-note">Oferta v√°lida por tiempo limitado</div>
      </div>
    </div>
  );
};

PromoCard.propTypes = {
  label: PropTypes.string,
  headline: PropTypes.string.isRequired,
  description: PropTypes.string,
  ratingText: PropTypes.string,
  dateISO: PropTypes.string,
  dateLabel: PropTypes.string,
  discount: PropTypes.number,
  categories: PropTypes.arrayOf(PropTypes.string),
  ctaText: PropTypes.string,
  ctaHref: PropTypes.string,
  onCtaClick: PropTypes.func,
};

export default PromoCard;
